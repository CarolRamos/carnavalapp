package com.uglyboys.canavalapp.config;

public class Atracao {
	
	private String bloco;
	private String atracaoNome;
	private String horarioConcentracao;
	
	public Atracao(){
	}

	public String getBloco() {
		return bloco;
	}

	public void setBloco(String bloco) {
		this.bloco = bloco;
	}

	public String getAtracaoNome() {
		return atracaoNome;
	}

	public void setAtracaoNome(String atracaoNome) {
		this.atracaoNome = atracaoNome;
	}

	public String getHorarioConcentracao() {
		return horarioConcentracao;
	}

	public void setHorarioConcentracao(String horarioConcentracao) {
		this.horarioConcentracao = horarioConcentracao;
	}

}
